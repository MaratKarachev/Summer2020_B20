package day05_Arithmetic0perators;

public class SelfPractice {
    public static void main(String[] args) {
        int a = (5+2)*4;
        //       7*4=28
        System.out.println(a);

        int b = 10/(5-3);
        //      10/(2)=5
        System.out.println(b);

        int c = 8+12*(6-2);
        //      8+12*4=8+48=56
        System.out.println(c);

        int d = (4+17)%2-1;
        //       21%2-1
        //       21/2=21-(2*10)=1-1=0
        System.out.println(d);

        int e = (6-3)*(2+7)/3;
        //       3*9/3
        //       27/3=9
        System.out.println(e);


    }

}
