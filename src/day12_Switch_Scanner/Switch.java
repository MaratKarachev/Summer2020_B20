package day12_Switch_Scanner;

public class Switch {
    public static void main(String[] args) {
       int a=1;
        switch(a){

            case 5:
                System.out.println("five");


            case 1:
                System.out.println("one");


            default:
                System.out.println("invalid case");

                System.out.println("=======================");


            }
        }
    }
