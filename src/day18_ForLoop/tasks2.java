package day18_ForLoop;

public class tasks2 {
    public static void main(String[] args) {
        char a;
        for (a = 'a'; a <= 'z'; ++a) {
            System.out.println(a + " ");
        }
        System.out.println("==================");
        char A;
        for (A = 'A'; A <= 'Z'; ++A) {
            System.out.println(A + " ");
        }
    }
}
